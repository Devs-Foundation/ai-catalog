async function seed() {
  console.log("A limpar dados existentes...");
  await db.delete(modelsTable);
  await db.delete(providersTable);

  console.log(`A inserir ${providers.length} fornecedores...`);
  const insertedProviders = await db
    .insert(providersTable)
    .values(providers)
    .returning();
  const slugToId = new Map(insertedProviders.map((p) => [p.slug, p.id]));

  console.log(`A inserir ${models.length} modelos...`);
  const modelRows = models.map(({ providerSlug, ...rest }) => {
    const providerId = slugToId.get(providerSlug);
    if (!providerId) {
      throw new Error(`Fornecedor desconhecido: ${providerSlug}`);
    }
    return { ...rest, providerId };
  });
  await db.insert(modelsTable).values(modelRows);

  console.log("Catálogo semeado com sucesso.");
  process.exit(0);
}

seed().catch((err) => {
  console.error(err);
  process.exit(1);
});
